public class EjemplosIFELSE 
{
	public static void main (String[] args) 
	{
		  int numero = 18;
		  if (numero % 2 == 0)
		  {
			 System.out.println("El número " + numero + " es par");
		  }
		  else
		  {
			 System.out.println("El número " + numero + " es impar");
		  }
		  System.out.println("Fin de programa");
	}
}
