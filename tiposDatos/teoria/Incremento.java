public class Incremento {
	
	public static void main (String[] args) {
		
		int variable = 10;
		System.out.println(variable++);  //muestra 10 y posteriormente incrementa la variable a 11
		System.out.println(variable);  //muestra 11
		System.out.println(++variable); //incrementa la variable a 12 y la muestra


	}
}

